#include "../../headers/libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*str;
	int		i;

	if (!s || !f)
		return (0);
	i = -1;
	str = malloc(sizeof(char) * ((int)ft_strlen(s) + 1));
	if (!str)
		return (0);
	while (s[++i])
		str[i] = (*f)(i, s[i]);
	str[ft_strlen(s)] = '\0';
	return (str);
}
